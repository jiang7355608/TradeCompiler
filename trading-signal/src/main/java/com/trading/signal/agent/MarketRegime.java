package com.trading.signal.agent;

/**
 * 市场行情状态分类，用于自动策略选择。
 * 
 * 根据技术指标（EMA、ATR、突破情况）将市场分为四种状态，
 * 每种状态对应推荐的策略类型。
 * 
 * 状态定义：
 * ─────────────────────────────────────────────────
 * TRENDING  │ 趋势行情 │ 推荐使用 AggressiveStrategy
 *           │          │ 特征：EMA斜率显著，ATR持续扩张，突破有效率高
 * ─────────────────────────────────────────────────
 * RANGING   │ 震荡行情 │ 推荐使用 MeanReversionStrategy
 *           │          │ 特征：价格在区间内反复，ATR收窄，突破后快速回测
 * ─────────────────────────────────────────────────
 * CHOPPY    │ 噪声行情 │ 保持当前策略，不主动切换
 *           │          │ 特征：高ATR但无方向，假突破率高，两种策略均不适合
 * ─────────────────────────────────────────────────
 * UNKNOWN   │ 数据不足 │ 保持当前策略
 *           │          │ 原因：K线不够、API异常等
 * ─────────────────────────────────────────────────
 */
public enum MarketRegime {

    /**
     * 趋势行情：EMA排列整齐，价格单向运动，ATR稳步上升
     * 推荐策略：aggressive（突破追踪）
     */
    TRENDING,

    /**
     * 震荡行情：价格在支撑/压力区间内反复，ATR相对收窄
     * 推荐策略：mean-reversion（均值回归）
     */
    RANGING,

    /**
     * 噪声行情：无方向性高波动，假突破频繁，策略切换无意义
     * 推荐策略：保持当前策略，等待市场澄清
     */
    CHOPPY,

    /**
     * 状态未知：数据不足或分析异常
     * 推荐策略：保持当前策略
     */
    UNKNOWN
}
